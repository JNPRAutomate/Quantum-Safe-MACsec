#!/usr/bin/env python3

import ast
import re
import sys
from pathlib import Path

try:
    import yaml
except ImportError:
    print("ERROR: PyYAML is required on the build host.")
    print("Install with: pip install PyYAML")
    sys.exit(1)


BASE_DIR = Path(__file__).resolve().parents[1]

QKD_DIR = BASE_DIR / "qkd"
CONFIG_FILE = BASE_DIR / "config" / "targets.yaml"
DIST_DIR = BASE_DIR / "dist"
DIST_FILE = DIST_DIR / "qkd.py"


MODULE_ORDER = [
    "qkd_runtime.py",
    "qkd_logging.py",
    "qkd_ssh.py",
    "qkd_state.py",
    "qkd_certs.py",
    "qkd_kme.py",
    "qkd_config.py",
    "qkd_process.py",
    "qkd.py",
]


INTERNAL_IMPORT_RE = re.compile(
    r"^\s*(from\s+qkd_[A-Za-z0-9_]+\s+import\b|import\s+qkd_[A-Za-z0-9_]+\b)"
)


def strip_internal_imports(source):
    """
    Remove imports between qkd modules because all modules are flattened
    into one generated on-box script.
    Handles both one-line and multiline imports:

        from qkd_state import (
            a,
            b,
        )
    """
    result = []
    skip_block = False
    paren_depth = 0

    for line in source.splitlines(keepends=True):
        if skip_block:
            paren_depth += line.count("(")
            paren_depth -= line.count(")")
            if paren_depth <= 0:
                skip_block = False
            continue

        if INTERNAL_IMPORT_RE.match(line):
            paren_depth = line.count("(") - line.count(")")
            if paren_depth > 0:
                skip_block = True
            continue

        result.append(line)

    return "".join(result)


def strip_main_guard(source):
    """
    Remove the final:
        if __name__ == '__main__':
            main()

    from qkd.py. We'll append our own clean guard at the end.
    """
    lines = source.splitlines(keepends=True)
    output = []
    skip = False

    for line in lines:
        if line.strip().startswith("if __name__") and "__main__" in line:
            skip = True
            continue

        if skip:
            # Skip indented block after guard.
            if line.startswith((" ", "\t")) or not line.strip():
                continue
            skip = False

        output.append(line)

    return "".join(output)


def remove_shebang(source):
    lines = source.splitlines(keepends=True)
    if lines and lines[0].startswith("#!"):
        return "".join(lines[1:])
    return source


def build_embedded_targets():
    if not CONFIG_FILE.exists():
        raise FileNotFoundError(f"Missing targets file: {CONFIG_FILE}")

    with open(CONFIG_FILE, "r") as f:
        targets = yaml.safe_load(f)

    if not isinstance(targets, dict):
        raise ValueError("targets.yaml did not parse into a dictionary")

    # repr() is enough here because YAML was converted to native Python types.
    targets_repr = repr(targets)

    return f'''
# ===== embedded targets.yaml =====

TARGETS_DICT = {targets_repr}


def load_targets(filename=None):
    """
    Return embedded targets.

    This generated on-box artifact does not read config/targets.yaml.
    The inventory was embedded at build time by build_onbox.py.
    """
    return TARGETS_DICT

'''


def build():
    DIST_DIR.mkdir(exist_ok=True)

    output = []

    output.append("#!/usr/bin/env python3\n")
    output.append('''"""
Generated QKD on-box single-file artifact.

This file is generated by build/build_onbox.py.
Do not edit this file directly.
Edit the modular sources under qkd/ and config/targets.yaml instead.
"""

''')

    for module_name in MODULE_ORDER:
        module_path = QKD_DIR / module_name

        if not module_path.exists():
            raise FileNotFoundError(f"Missing module: {module_path}")

        source = module_path.read_text()

        source = remove_shebang(source)
        source = strip_internal_imports(source)

        if module_name == "qkd_targets.py":
            # We do not include qkd_targets.py because we embed targets.yaml instead.
            continue

        if module_name == "qkd.py":
            source = strip_main_guard(source)

        output.append(f"\n# ============================================================\n")
        output.append(f"# BEGIN {module_name}\n")
        output.append(f"# ============================================================\n\n")
        output.append(source)
        output.append(f"\n# ============================================================\n")
        output.append(f"# END {module_name}\n")
        output.append(f"# ============================================================\n")

        if module_name == "qkd_runtime.py":
            output.append(build_embedded_targets())

    output.append('''

if __name__ == "__main__":
    main()
''')

    DIST_FILE.write_text("".join(output))
    print(f"Generated: {DIST_FILE}")


if __name__ == "__main__":
    build()